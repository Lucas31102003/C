#include <stdio.h>
int main() 
{ 
	//Declarar as Variáveis
	int a = 50;
	int b = 30;
	
	//Operadores aritméticos + - * /
	printf("A soma é %d \n" , (a+b) );
	printf("A subtração é %d \n" , (a-b) );
	printf("A multiplicação é %d \n" , (a*b) );
	printf("A divisão é %d \n" , (a/b) );
	printf("O resto da divisão é %d \n" , (a%b) );
	
	
	return 0;
}
